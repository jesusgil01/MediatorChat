    private boolean login(String name){
        for (User user:users) {
            if (user.getName().compareTo(name) == 0){
                return true;
            }
        }
        return false;
    }

    //Usa este m√©todo para agregar usuarios nuevos, si pueden haganlo con archivos para que se guarden los usuarios
    private void addUser(String name){
        if (users.contains(name)){
            JOptionPane.showMessageDialog(this, "El usuario " + name + " ya se encuentra registrado");
        } else {
            users.add(new User(name));
        }
    }

    private void inciarSesionbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inciarSesionbtnActionPerformed
        // TODO add your handling code here:
        String name = usuarioTxt.getText();
        if (login(name)){
            JOptionPane.showMessageDialog(this, "Bienvenido!!!");
            ChatVisual chatVisual = new ChatVisual(name);

            chatVisual.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "El usuario no existe");
        }

    }//GEN-LAST:event_inciarSesionbtnActionPerformed

